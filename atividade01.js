//Declaração de variáveis
var a = 6;
var b = 8;
var c = 11;

// Operação Soma
var d = a + b + c;
console.log(`A soma de ${a}, ${b} e ${c} é ${d}`)

// Operação Subtração
var e = c - b;
console.log(`A subtração de ${c} e ${b} é ${e}`)

// Operação Multiplicação
var f = a * b * c;
console.log(`A multiplicação de ${a}, ${b} e ${c} é ${f}`)

// Operação Divisão
var g = b / a;
console.log(`A divisão de ${b} e ${a} é ${g}`)